import math

num = 64

sq_root = math.sqrt(num)
print(f'root of {num}: {sq_root}')

cb_root = math.cbrt(num)
print(f'root of {num}: {cb_root}')

ceil = math.ceil(10.5)
print(f'ceil: {ceil}')

floor = math.floor(10.5)
print(f'floor: {floor}')

fact = math.factorial(5)
print(f'factorial: {fact}')

power = math.pow(4, 3)
print(f'4^3: {power}')

